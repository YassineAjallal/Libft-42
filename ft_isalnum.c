#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	s;

	s = (unsigned char)c;
	if ((s > 64 && s < 91) || (s > 96 && s < 123) || (s > 47 && s < 58))
		return (1);
	else
		return (0);
}
